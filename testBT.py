    engine = sqlalchemy.create_engine('sqlite:///BUSDquote.db')

    price_btc = pd.read_sql('select Time, Close from crypto_price where Symbol == "BTCBUSD"', engine)
    price_btc.set_index('Time', inplace=True)
    price_btc.index = pd.to_datetime(price_btc.index)

    # Create a cerebro entity

    
    cerebro = bt.Cerebro()

    # Add a strategy
    cerebro.addstrategy(smaCross)


    # Add the Data Feed to Cerebro
    cerebro.adddata(bt.feeds.PandasData(dataname = price_btc, open='Close', high='Close', low='Close',close='Close'))

    # Set our desired cash start
    cerebro.broker.setcash(100.0)

    # Add a FixedSize sizer according to the stake
    cerebro.addsizer(bt.sizers.PercentSizer, percents=100)

    # Set the commission
    cerebro.broker.setcommission(commission=0.0)

    # Print out the starting conditions
    print('Starting Portfolio Value: %.2f' % cerebro.broker.getvalue())

    # Run over everything
    cerebro.run()

    # Print out the final result
    print('Final Portfolio Value: %.2f' % cerebro.broker.getvalue())

    # Plot the result
    cerebro.plot(volume=False)